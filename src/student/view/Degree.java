    private void btn_deg_proActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deg_proActionPerformed

        try {

            if (inputDataValidator() == true && allReady() == true) {

                String deg_pro = txt_deg_pro.getText().trim().toUpperCase();

                int i = dp.btn_deg_proHandleClick(deg_pro);
                if (i != 0) {
                    JOptionPane.showMessageDialog(jPanel1, "Successfully Addeda!", "Done", JOptionPane.PLAIN_MESSAGE);
                    txt_deg_pro.setText("");
                    showYnSList();
                } else {
                    JOptionPane.showMessageDialog(jPanel1, "Failed!", "Error", JOptionPane.ERROR_MESSAGE);
                }

            }

        } catch (SQLException ex) {

//            Logger.getLogger(Degree.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(jPanel1, "Failed! MYSQL ERROR", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btn_deg_proActionPerformed

    private void tbl_deg_porMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_deg_porMouseClicked

        int selectedRow = tbl_deg_por.getSelectedRow();
        int Id = (int) tbl_deg_por.getValueAt(selectedRow, 0);
        String valueAt = (String) tbl_deg_por.getValueAt(selectedRow, 1);
        String valuId = String.valueOf(Id);
        id.setText(valuId);
        txt_deg_pro.setText(valueAt);

    }//GEN-LAST:event_tbl_deg_porMouseClicked

    public void showYnSList() {
        HashMap hm = new HashMap();
        hm.put("idDegPro", "int");
        hm.put("degName", "String");

        new LoadTable(tbl_deg_por, "Select * from DegreePro", hm);
    }

    public boolean inputDataValidator() throws SQLException {

        String degPro = txt_deg_pro.getText().toString();

        if (degPro.isEmpty()) {
            errorMsg.setText("Degree Program is Required.");
            errorMsg.setVisible(true);
            return false;
        }
        return true;
    }

    public boolean allReady() {

        String degPro = txt_deg_pro.getText().toString().toUpperCase();
        

        try {
            if (!dp.getAllReady(degPro)) {
                
                errorMsg.setText("Degree Program Already Exist");
                errorMsg.setVisible(true);
                return false;

            }
        } catch (SQLException ex) {
            Logger.getLogger(Degree.class.getName()).log(Level.SEVERE, null, ex);
        }

        return true;
    }


