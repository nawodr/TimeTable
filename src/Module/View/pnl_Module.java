    private ModuleModel CreateModule() {

       ModuleModel module = new ModuleModel();

        try {
            int a = Integer.parseInt(cmp_lec.getSelectedItem().toString());
            int x = Integer.parseInt(cmb_thrs.getSelectedItem().toString());
            int y = Integer.parseInt(cmp_labhrs.getSelectedItem().toString());
            int z = Integer.parseInt(cmp_evahrs.getSelectedItem().toString());

            module.setOfferedyear(cmb_offeredyear.getSelectedItem().toString());
            module.setOfferedsem(cmb_offeredsem.getSelectedItem().toString());
            module.setMname(txt_mname.getText().trim());
            module.setMcode(txt_mcode.getText().trim());
            module.setLechrs(a);
            module.setThrs(x);
            module.setLabhrs(y);
            module.setEvahrs(z);

        } catch (Exception e) {
        }
        return module;
    }

